const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// User Model
const UserSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  }
});

module.exports = UserDTO = mongoose.model('user', UserSchema);